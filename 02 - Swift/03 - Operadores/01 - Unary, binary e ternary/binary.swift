// Operador Binary

/*

    É um operador que atua sobre dois operandos para executar a sua função.

    Exemplos:

    ★ a + b → soma;
    ★ a - b → subtração;
    ★ a * b → multiplicação;
    ★ a / b → divisão;
    ★ a % b → resto;
    ★ a && b → AND lógico;
    ★ a || b → OR lógico.
*/

let x = 10
let y = 3
let soma = x + y // soma → 13
let subtracao = x - y // subtração → 7
let multiplicacao = x * y // multiplicação → 30
let divisao = x / y // divisão inteira → 3
let resto = x % y // divisão com resto → 1

let andResultado = true && false   // lógico AND → false
let orResultado = true || false    // lógico OR → true